function filterSelect(filter, filterTitle) {
  let list;

  switch(filter) {
    case "dateOfBirth" : filterType = dateOfBirth;break;
    case "hatSize" : filterType = hatSize;break;
    case "lengthFemale": filterType = lengthFemale;break;
    case "waistFemale": filterType = waistFemale;break;
    case "legnthMale": filterType = legnthMale;break;
    case "waistMale": filterType = waistMale;break;
    case "shirtSize": filterType = shirtSize;break;
    case "shoeSizeFemale": filterType = shoeSizeFemale;break;
    case "shoeSizeMale": filterType = shoeSizeMale;break;
    case "height": filterType = height;break;
    case "bodyType": filterType = bodyType;break;
    case "strongestVisualAsset": filterType = strongestVisualAsset;break;
    case "ethnicity": filterType = ethnicity;break;
    case "eyeColor": filterType = eyeColor;break;
    case "hairColor": filterType = hairColor;break;
    default: break;
  }

  for (var filterItemN = 0; filterItemN < filterType.length; filterItemN++) {
    
    let filterItem = filterType[filterItemN];

    list += '<option value="' + filterItem + '">' + filterItem + '</option>';
  }

  //list = list.substring(9);

  list = '<select>'+
    '<option disabled="" selected="">' + filterTitle +'</option>'+
    list +
    '</select>';

  return list;
}


